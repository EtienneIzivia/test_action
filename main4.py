def calculate_sum(a, b):
    """
    Calculate and return the sum of two values.

    Parameters:
    a (int): The first value.
    b (int): The second value.

    Returns:
    int: The sum of a and b.
    """
    return a + b


# Example usage
RESULT = calculate_sum(3, 5)
print("The sum is:", RESULT)
